for($i=1024;$i<1084;$i++){
  // $arr[]=$i;
  $arr[$i]=$sky[($i-4)%10] . $land[($i-4)%12];
  // $arr[$i]=$sky[($i-4)%10] . $land[($i-4)%12]; 
