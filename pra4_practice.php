  // 課堂是這樣寫法：$arr[$i]=$sky[($i%10+6)%10] . $land[($i%12+8)%12];

}

for($i=1024;$i<3000;$i++){

  $arr[$i]=$sky[($i-4)%10] . $land[($i-4)%12];

}

